func eventSignHash(eventTopic string) string {
	eventSignature := []byte(eventTopic)
	hash := crypto.Keccak256Hash(eventSignature)
	return hash.Hex()
}

func getABI(abiJSON string) abi.ABI {
	wrapABI, _ := abi.JSON(strings.NewReader(abiJSON))
	return wrapABI
}

